numero = int(input ("Digite um número:"))
#Aqui eu pedi um número pro meu usuário.
lista = list(range(0, numero + 1))
#aqui eu criei uma lista de 0 até o número digitado.
for valor in lista:
    print(valor)
#Aqui o for percorreu toda a lista e o print mostrou cada valor dela.

#O código todo cria uma lista de 0 até o número que meu usuário digitou e mostra todos eles.